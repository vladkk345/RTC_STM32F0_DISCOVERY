void Display_Time(void)
{
	LCD_SetPos(3,0);
	sprintf((char *)time,"%02d:%02d:%02d",gTime.Hours, gTime.Minutes, gTime.Seconds);
	LCD_String(time);

	LCD_SetPos(3,1);
	sprintf((char *)date,"%02d:%02d:%02d",gDate.Date, gDate.Month, 2000 + gDate.Year);
	LCD_String(date);
}

